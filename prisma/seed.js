function toTagSlug(input) {
  return input
    .trim()
    .toLowerCase()
    .replace(/[^a-zа-я0-9]+/gi, "-")
    .replace(/^-+|-+$/g, "");
}

async function seed() {
  const { PrismaClient } = await import("@prisma/client");
  prisma = new PrismaClient();

  const author = await prisma.user.upsert({
    where: { email: "kulinaren-blog-ivo@example.local" },
    update: { name: "Кулинарният блог на Иво" },
    create: {
      email: "kulinaren-blog-ivo@example.local",
      name: "Кулинарният блог на Иво",
      role: "ADMIN",
    },
  });

  for (const recipe of recipes) {
    await prisma.recipe.deleteMany({
      where: { slug: recipe.slug },
    });

    await prisma.recipe.create({
      data: {
        slug: recipe.slug,
        title: recipe.title,
        excerpt: recipe.excerpt,
        content: recipe.content,
        heroImage: recipe.heroImage,
        prepMinutes: recipe.prepMinutes,
        cookMinutes: recipe.cookMinutes,
        servings: recipe.servings,
        difficulty: recipe.difficulty,
        published: true,
        createdAt: recipe.publishedAt,
        author: {
          connect: { id: author.id },
        },
        category: {
          connectOrCreate: {
            where: { slug: recipe.category.slug },
            create: {
              name: recipe.category.name,
              slug: recipe.category.slug,
            },
          },
        },
        ingredients: {
          create: recipe.ingredients.map((ingredient, index) => ({
            amount: ingredient.amount,
            name: ingredient.name,
            position: index,
          })),
        },
        steps: {
          create: recipe.steps.map((instructions, index) => ({
            instructions,
            position: index,
          })),
        },
        tags: {
          create: recipe.tags.map((tag) => ({
            tag: {
              connectOrCreate: {
                where: { slug: toTagSlug(tag) },
                create: {
                  name: tag,
                  slug: toTagSlug(tag),
                },
              },
            },
          })),
        },
      },
    });
  }

  console.log(`Seeded ${recipes.length} recipes.`);
}

seed()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    if (prisma) {
      await prisma.$disconnect();
    }
  });